    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
        try {
            String sql = "INSERT INTO usuarios (usuario_nomape, usuario_nom, usuario_pass, usuario_tipo) "
                    + "VALUES (?,?,?,?)";
            PreparedStatement ps = conectar.prepareCall(sql);
            ps.setString(1, txtnomape.getText());
            ps.setString(2, txtuser.getText());
            ps.setString(3, txtpass.getText());
            ps.setString(4, cmbtipo.getSelectedItem().toString());
            int n=ps.executeUpdate();
            if(n>0){
                JOptionPane.showMessageDialog(null, "Registro ingresado con Ã©xito");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al agregar usuario " + e.getMessage());
        }
    }//GEN-LAST:event_btnagregarActionPerformed

